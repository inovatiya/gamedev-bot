def solve_math(text):
    m = re.search(r'(\d+)\s*([\+\-\*\/])\s*(\d+)', text)
    if not m:
        return None
    a = int(m.group(1))
    op = m.group(2)
    b = int(m.group(3))
    if op == '+':
        return f"{a} + {b} = {a+b}"
    elif op == '-':
        return f"{a} - {b} = {a-b}"
    elif op == '*':
        return f"{a} * {b} = {a*b}"
    elif op == '/':
        if b == 0:
            return "Нельзя делить на 0"
        return f"{a} / {b} = {a/b}"
    return None

