@bot.message_handler(func=lambda m: True)
def handle(m):
    t = m.text.lower()
    ans = solve_math(m.text)
    if ans:
        bot.send_message(m.chat.id, ans)
        return
    if "526 математика" in t:
        bot.send_message(m.chat.id, get_task("math"))
    elif "русский" in t:
        bot.send_message(m.chat.id, get_task("russian"))
    elif "526 английский" in t or "английский" in t:
        bot.send_message(m.chat.id, get_task("english"))
    elif "окружающий" in t or "мир" in t:
        bot.send_message(m.chat.id, get_task("environment"))
    elif "впр" in t:
        bot.send_message(m.chat.id, get_task("vpr"))
    elif "логика" in t:
        bot.send_message(m.chat.id, get_task("logic"))
    elif "задача" in t:
        c = ["math","russian","english","environment","vpr","logic"]
        bot.send_message(m.chat.id, get_task(random.choice(c)))
    else:
        bot.send_message(m.chat.id, "Напиши /start")

