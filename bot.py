@bot.message_handler(func=lambda m: True)
def handle_all(message):
    text = message.text.strip()
    
    # Сначала проверяем команды (они уже обработаны выше, но это на всякий случай)
    if text.startswith('/'):
        return
    
    # Проверяем, не математический ли это пример
    math_result = solve_math_example(text)
    if math_result:
        bot.send_message(message.chat.id, math_result)
        return
    
    # Проверяем, не ответ ли это на задачу
    if text.lower().startswith("ответ:"):
        user_answer = text[6:].strip()
        
        if message.chat.id in user_last_task:
            task = user_last_task[message.chat.id]
            result = check_answer(user_answer, task["answer"])
            bot.send_message(message.chat.id, result)
        else:
            bot.send_message(message.chat.id, "❓ Сначала получи задачу (например /math526)")
        return
    
    # Если это не пример и не ответ — просто общаемся
    response = chat_response(text)
    if response:
        bot.send_message(message.chat.id, response)
    else:
        # Если не знаем, что ответить — предлагаем задачу
        bot.send_message(message.chat.id, "📚 Хочешь решить задачку? Напиши /task")

